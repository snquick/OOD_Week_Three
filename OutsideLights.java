
public class OutsideLights {
	
	public OutsideLights() {
	}
	
	public void on() {
		System.out.println("Bush, roof, and porch lights are on! Merry Christmas.");
	}

	public void off() {
		System.out.println("Outdoor lights are now off.. Good night.");
	}
	
	public void twinkle() {
		System.out.println("Twinkle mode activated -- Extreme Christmas Celebration!");
	}
	
	public void partyMode() {
		System.out.println("Disco colors, jump sequence; let's bring the neighbors over!");
	}

}
